                        <p class="summary">Summary</p>
                        <p>The challenge for E&Ps in unconventional fields is the same as always:<br>
                            How do you balance the risk of poor well production with the cost of gathering data to
                            improve production? If information on a well’s completion strategy effectiveness were free,
                            operators would gather data on every well to continuously hone their field development
                            plans. Unfortunately, developing unconventional resources has shown that conventional
                            practices are insufficient to deliver superior operational and financial performance.</p>
                        <p>New technology and methods of integrating data sets improve reservoir understanding by
                            reducing the underlying uncertainty. This webcast with case studies will discuss how proven
                            technologies, including fiber optics and pressure-based fracture maps, are providing
                            cost-effective solutions for enhancing field development.</p>
