                <h3>How does this all work?</h3>
                <p>ProppantSCAN technology uses our pressure-based proppant mapSM computed with our patented data acquisition1 and processing2. The data source is a surface pressure gauge on a monitor well—established through isolation from prior stages—that records the poroelastic pressure response from a nearby treatment well during hydraulic fracturing. As the treatment well is fracked, our geoscientists and completion engineers identify and quantify the poroelastic signals from the monitor well’s measured pressure response post-treatment.</p>
                <p>We use the poroelastic signals to generate a pressure versus time curve. Once the pumps are shut off and the treatment ends, the pressure begins to drop and eventually levels off. The initial drop in the pressure represents the fluid leakoff before the fracture begins to close onto any proppant embedded in the fluid and left behind in the fracture, also known as the proppant landing point. We use our proprietary digital twin and proppant-area solver to compute the minimum and maximum propped dimensions.</p>
                <p>With the proppant map and ProppantSCAN technology, you can obtain an accurate estimate of the effective drainage volume.</p>
                <p></p>
                <h3 class="define">Customer Experience: Not around the bend</h3>
                <p>What effect does well orientation have on propped fracture geometry?</p>
                <p>Is minimum horizontal stress always the best wellbore orientation?</p>
                <img src="<?php echo get_template_directory_uri(); ?>/img/propant_1.png" alt="">
                <p>An operator working on two, four-well pads in the Eagle Ford called on us to analyze the effect of well orientation on propped fracture geometry. One set of wells was drilled in the direction of the minimum horizontal stress (SHmin) and the other set of wells was drilled at a 45° angle to the SHmin. Both pads had identical completion designs. We went to work with ProppantSCAN technology to find out which pad had the better-propped fracture geometry.</p>
                <p>The technology showed that both pads had very similar hydraulic fracture half-lengths and heights. But, there was one significant difference. The propped fracture geometry of the wells drilled at a 45° angle to the SHmin was 50% smaller than the propped fracture geometry of the wells drilled in the same direction of the SHmin. This understanding is important from a proppant transport point of view for wells drilled at a 45° angle to the direction of the minimum horizontal stress. For these wells, fracture reorientation is likely to occur, limiting proppant transport because of the increased fracture complexity.</p>
                <p>With ProppantSCAN technology and the greater insight into the propped fracture geometry from the proppant map, in addition to other datapoints, the operator made informed, better decisions about well spacing in the Eagle Ford.</p>
